package _01_Basic;

public class TypeCasting {//conversion of Bigger Data type into Smaller Data Type
    public static void main(String[] args) {
        float mark=45.4F;
        int roundNum=(int)(mark);
        System.out.println(roundNum);

        char alp='f';
        int charVal=(int)(alp);
        System.out.println(charVal);
    }
}
